var a = 3;
// var significa Variáveis
console.log(a + a);

let b = 4;
// let também serve para criar variáveis, é mais usado e recomendado usar mais do que var
console.log(b * b);

var a = 30;
b = 40;
console.log(a, b);

a = 300;
b = 400;
console.log(a, b);

const c = 5;
// const significa constante, ao contrário das variáveis, a constante não pode ser alterada
console.log(c);